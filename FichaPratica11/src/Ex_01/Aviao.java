package Ex_01;

public class Aviao {
    private String numSerie;
    private String modelo;
    private String anoFabrico;
    private double pesoKg;
    private double compFuselagem;
    private double enverAsas;
    private double hCauda;
    private int numMotores;
    private double autonomiaKm;
    private double velMax;
    private double preco;


    public Aviao(String numSerie, String modelo, String anoFabrico, double pesoKg, double compFuselagem, double enverAsas, double hCauda, int numMotores, double autonomiaKm, double velMax, double preco) {
        this.numSerie = numSerie;
        this.modelo = modelo;
        this.anoFabrico = anoFabrico;
        this.pesoKg = pesoKg;
        this.compFuselagem = compFuselagem;
        this.enverAsas = enverAsas;
        this.hCauda = hCauda;
        this.numMotores = numMotores;
        this.autonomiaKm = autonomiaKm;
        this.velMax = velMax;
        this.preco = preco;
    }

    





}
