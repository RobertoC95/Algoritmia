package FichaPraticaExtraCiclos;

public class Ex04_1 {
    public static void main(String[] args) {

        int linhas = 4, colunas = 10;

        for (int i = 0; i < linhas; i++) {

            for (int j = 0; j < colunas; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
